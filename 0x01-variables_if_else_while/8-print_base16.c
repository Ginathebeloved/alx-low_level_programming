#include <stdio.h>
/**
 * main - Determines if a number is positive, negative or zero.
 *Description: 'get the last difit of a number'
 * Return: Always 0
 */

int main(void)
{
	int n;
	int m;

	for (n = 48; n <= 57; n++)
	{
		putchar(n);
	}
	for (m = 97; m <= 102; m++)
	{
		putchar(m);
	}
	putchar('\n');
	return (0);
}
