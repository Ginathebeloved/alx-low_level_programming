#include <stdio.h>
/**
 * main - Determines if a number is positive, negative or zero.
 *Description: 'get the last difit of a number'
 * Return: Always 0
 */

int main(void)
{
	int n = 97;
	int m = 65;

	while (n <= 122)
	{
		putchar(n);
		n++;
	}
	while (m <= 90)
	{
		putchar(m);
		m++;
	}
	putchar('\n');
	return (0);
}
